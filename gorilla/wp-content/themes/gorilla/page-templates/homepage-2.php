                                                  <img src="<?php echo get_stylesheet_directory_uri();  ?>/upload/baby-gorilla-in-the-brush.jpg" alt="A picture of a baby gorilla in the brush in the Congo Basin"> The Belgian Congo established Virunga in 1925 as one of Africa's first national parks. It is now a UNESCO World Heritage site with lush volcanic forests, unparalleled biodiversity, and home to the endangered species of mountain gorillas. Poaching, logging, and armed conflicts in the DRC have hurt the park rangers' efforts to protect the gorillas. </br></br>Hundreds of rangers have been killed over the years. In early 2020, they were on their way to the park when they saw a civilian vehicle on the side of the road. They stopped to offer assistance and fell into an ambush orchestrated by the rebel group called the "Democratic Forces for the Liberation of Rwanda". <a href="https://virunga.org/news/updated-statement-from-virunga-national-park-on-recent-attack/" target="_blank">The rebels murdered 17 people, including 12 rangers</a>. According to <a href="https://www.nytimes.com/2020/04/25/world/africa/congo-virunga-national-park-attack.html?smid=url-share" target="_blank">Abdi Latif Dahir of the New York Times</a>, these rebels are among the largest foreign armed groups in the Congo, whose members include people wanted for the 1994 genocide in Rwanda.</br></br>


